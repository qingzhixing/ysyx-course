addi x1, x0, 0 		# x1 = 0
addi x2, x0, 0x11 	# 准备写入值 0x11
sb x2, 0(x1) 		# 写入字节 0x11 到 RAM 字节地址 0
addi x2, x0, 0x22 	# 准备 0x22
sb x2, 1(x1) 		# 写入字节 0x22 到地址 1
addi x2, x0, 0x33	# 准备 0x33
sb x2, 2(x1) 		# 写入字节 0x33 到地址 2
addi x2, x0, 0x44	# 准备 0x44
sb x2, 3(x1) 		# 写入字节 0x44 到地址 3
lbu x10, 0(x1) 		# 读回字节 0x11
lbu x11, 1(x1) 		# 读回字节 0x22
lbu x12, 2(x1) 		# 读回字节 0x33
lbu x13, 3(x1) 		# 读回字节 0x44
jalr x0, 0x34(x0) 	# 无限循环（跳转到自身）